package com.cbp.web.dto;

public class PlasticDTO {
	
   
    private Long idPlastic;
    private Long plasticAsigned;
    private long plasticCvv;
    private String plasticDateIssue;
    private String plasticExpDate;
    private String plasticNumber;
    private String plasticStatus;
	
	public Long getIdPlastic() {
		return idPlastic;
	}
	public void setIdPlastic(Long idPlastic) {
		this.idPlastic = idPlastic;
	}
	public Long getPlasticAsigned() {
		return plasticAsigned;
	}
	public void setPlasticAsigned(Long plasticAsigned) {
		this.plasticAsigned = plasticAsigned;
	}
	public long getPlasticCvv() {
		return plasticCvv;
	}
	public void setPlasticCvv(long plasticCvv) {
		this.plasticCvv = plasticCvv;
	}
	public String getPlasticDateIssue() {
		return plasticDateIssue;
	}
	public void setPlasticDateIssue(String plasticDateIssue) {
		this.plasticDateIssue = plasticDateIssue;
	}
	public String getPlasticExpDate() {
		return plasticExpDate;
	}
	public void setPlasticExpDate(String plasticExpDate) {
		this.plasticExpDate = plasticExpDate;
	}
	public String getPlasticNumber() {
		return plasticNumber;
	}
	public void setPlasticNumber(String plasticNumber) {
		this.plasticNumber = plasticNumber;
	}
	public String getPlasticStatus() {
		return plasticStatus;
	}
	public void setPlasticStatus(String plasticStatus) {
		this.plasticStatus = plasticStatus;
	}

}
